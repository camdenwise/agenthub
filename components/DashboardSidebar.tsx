type DashboardSidebarProps = {
  businessName?: string | null
  businessLocation?: string | null
  unreadMessagesCount?: number
}

export function DashboardSidebar({
  businessName,
  businessLocation,
  unreadMessagesCount = 0,
}: DashboardSidebarProps) {
  const pathname = usePathname()
  const { activeBusiness } = useAdmin()
  const displayBusinessName = activeBusiness?.name || businessName

  return (
    <aside className="flex h-screen w-[230px] flex-col bg-[#0f1729]">
      {/* Branding */}
      <div className="flex items-center gap-3 border-b border-slate-700/50 px-5 py-5">
        <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-full bg-violet-600">
          <svg className="h-5 w-5 text-white" fill="currentColor" viewBox="0 0 24 24">
            <path d="M13 10V3L4 14h7v7l9-11h-7z" />
          </svg>
        </div>
        <div>
          <p className="font-semibold leading-tight text-white">AgentHub</p>
          <p className="text-xs text-slate-400">AI Conversion Platform</p>
        </div>
      </div>

      {/* Nav tabs */}
      <nav className="flex-1 space-y-0.5 px-3 py-4">
        {DASHBOARD_NAV.map((item) => {
          const isActive = pathname === item.href || (item.href !== '/' && pathname.startsWith(item.href))
          const icon = ICONS[item.icon] ?? null
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
                isActive
                  ? 'border-l-2 border-violet-500 bg-slate-700/60 text-white'
                  : 'border-l-2 border-transparent text-slate-300 hover:bg-slate-700/40 hover:text-white'
              }`}
            >
              <span className={isActive ? 'text-violet-400' : ''}>{icon}</span>
              <span className="flex-1">{item.label}</span>
              {item.icon === 'messages' && unreadMessagesCount > 0 && (
                <span className="flex h-5 min-w-[20px] items-center justify-center rounded-full bg-red-500 px-1.5 text-xs font-medium text-white">
                  {unreadMessagesCount}
                </span>
              )}
            </Link>
          )
        })}
      </nav>

      {/* Business + logout at bottom */}
      <div className="border-t border-slate-700/50 px-4 py-4">
        {displayBusinessName && (
          <div className="mb-3 flex items-center gap-3">
            <div className="flex h-9 w-9 shrink-0 items-center justify-center rounded-full bg-emerald-700 text-sm font-medium text-white">
              {displayBusinessName.slice(0, 2).toUpperCase()}
            </div>
            <div className="min-w-0 flex-1">
              <p className="truncate text-sm font-medium text-white">{displayBusinessName}</p>
              {businessLocation && (
                <p className="truncate text-xs text-slate-400">{businessLocation}</p>
              )}
            </div>
          </div>
        )}
        <LogoutButton />
      </div>
    </aside>
  )
}
